# augADMMExample.R

#' Add Two Numbers
#'
#' This function takes two numeric values and returns their sum.
#'
#' @param num1 A numeric value.
#' @param num2 A numeric value.
#' @return The sum of num1 and num2.
#' @export
#' @examples
#' augADMMExample(5, 3) # returns 8
#' 
augADMMExample <- function(num1, num2) {
  sum <- num1 + num2
  return(sum)
}
